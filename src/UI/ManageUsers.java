    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        String empNo = txtSearchEmpNo.getText();
        User user = new User(empNo);
        DBManager dbManager = new DBManager();
        User userSearch = dbManager.searchUser(user);
        
        if (userSearch == null) {
             JOptionPane.showMessageDialog(null, "Invalid Employee Number", "ERROR", JOptionPane.ERROR_MESSAGE);
        }else{
            
        txtEmpNo.setText(userSearch.getEmpNo());
        txtUserName.setText(userSearch.getUserName());
        txtPass.setText(userSearch.getPass());
        cbRole.setSelectedItem(userSearch.getRole());
        txtFullName.setText(userSearch.getFullName());
        txtExtNo.setText(userSearch.getExtNo());
        txtEmail.setText(userSearch.getEmial());
        }
        
        
        
        
    }//GEN-LAST:event_btnSearchActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        String empNo = txtSearchEmpNo.getText();
        User user = new User(empNo);
        DBManager dbManager = new DBManager();
        boolean flag = dbManager.deleteUser(user);
        
        if (flag) {
            JOptionPane.showMessageDialog(null, "User has been sucessfully removed!");
            txtEmpNo.setText("");
            txtUserName.setText("");
            txtPass.setText("");
            cbRole.setSelectedItem("Operator");
            txtFullName.setText("");
            txtExtNo.setText("");
            txtEmail.setText("");
        } else {
            JOptionPane.showMessageDialog(null, "User removing has been failed!", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
        
        
    }//GEN-LAST:event_btnDeleteActionPerformed

