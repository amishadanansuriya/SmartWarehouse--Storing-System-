    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        String userName = txtUserName.getText();
        String pass = txtPass.getText();
        String role = (String) cbRole.getSelectedItem();
        String empNo = txtEmpNo.getText();
        String fullName = txtFullName.getText();
        String extNo = txtExtNo.getText();
        String email = txtEmail.getText();
        
        User user = new User(userName, pass, role, empNo, fullName, extNo, email);
        DBManager dbmanager = new DBManager();
        boolean flag = dbmanager.updateUser(user);
        
        if (flag) {
            JOptionPane.showMessageDialog(null, "User has been sucessfully updated!");
            txtEmpNo.setText("");
            txtUserName.setText("");
            txtPass.setText("");
            cbRole.setSelectedItem("Operator");
            txtFullName.setText("");
            txtExtNo.setText("");
            txtEmail.setText("");
        } else {
            JOptionPane.showMessageDialog(null, "User updation has been failed!", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

