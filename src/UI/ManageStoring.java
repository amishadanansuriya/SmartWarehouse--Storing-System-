    private void btnJobSearch1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnJobSearch1ActionPerformed
        String jobNo = txtSearchJobNo.getText();
        Storing storing = new Storing(jobNo);
        DBManager dbManager = new DBManager();
        Storing jobSearch = dbManager.jobSearch(storing);

        if (jobSearch == null) {
            JOptionPane.showMessageDialog(null, "Invalid Client Number of Job has been closed!", "ERROR", JOptionPane.ERROR_MESSAGE);
        } else {

            txtJobNo.setText(jobSearch.getJobNo());
            txtStartingDate.setText(jobSearch.getStartingDate());
            txtEndingDate.setText(jobSearch.getEndingDate());
    }//GEN-LAST:event_btnJobSearch1ActionPerformed
}
    
