    private void btnCloseStoringActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCloseStoringActionPerformed
       
        String jobID = txtSearchJobNo.getText();
        String closeDate = dateFormat.format(dateClose.getDate());
        String comment = txtCComment.getText();
        int  status = 0;
        Bill bill = new Bill(jobID, closeDate, comment, status);
        DBManager dbManager = new DBManager();
        boolean falg =  dbManager.closeJob(bill);
        if (falg) {
            JOptionPane.showMessageDialog(null, "Job has been closed!");
            txtSearchJobNo.setText("");
            txtJobNo.setText("");
            txtStartingDate.setText("");
            txtEndingDate.setText("");
            dateClose.setDate(date);
            txtCComment.setText("");
        } else{
            JOptionPane.showMessageDialog(null, "Job closing has been failed!", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnCloseStoringActionPerformed

